package _05_for_loops._2_badgers;

public class Badges {
	public static void main(String[] args) {
		
		for(int i = 0; i < 12; i++) {
			System.out.println("Badger");
		}
		for(int i = 0; i < 2; i++) {
			System.out.println("mushroom");
		}
		for(int i = 0; i < 12; i++) {
			System.out.println("Badger");
		}
	
		for(int i = 0; i < 2; i++) {
			System.out.println("mushroom");
		}
		for(int i = 0; i < 1; i++) {
			System.out.println("Snake!");
		}
	}
}

